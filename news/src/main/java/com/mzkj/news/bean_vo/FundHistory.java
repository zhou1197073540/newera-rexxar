package com.mzkj.news.bean_vo;

public class FundHistory {
    private String date_time;
    private String cumrate;
    private String bcumrate;

    public String getDate_time() {
        return date_time;
    }

    public void setDate_time(String date_time) {
        this.date_time = date_time;
    }

    public String getCumrate() {
        return cumrate;
    }

    public void setCumrate(String cumrate) {
        this.cumrate = cumrate;
    }

    public String getBcumrate() {
        return bcumrate;
    }

    public void setBcumrate(String bcumrate) {
        this.bcumrate = bcumrate;
    }
}
